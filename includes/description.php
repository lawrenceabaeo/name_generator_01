<h3>What is Diamond Dogs?</h3>

<p>Diamond Dogs is an organization in the video game <a href="https://en.wikipedia.org/wiki/Metal_Gear_Solid_V:_The_Phantom_Pain">'Metal Gear: The Phantom Pain'</a>. From the <a href="http://metalgear.wikia.com/wiki/Diamond_Dogs
  ">Wikia:</a>
  <blockquote cite="http://metalgear.wikia.com/wiki/Diamond_Dogs
" class="style2">Diamond Dogs was a private military company led by Venom Snake around the 1980s.</blockquote>
</p>

<h3>Why do they get code names?</h3>

<p>Code names provide some concealment for Diamond Dog members; the names also separate them from their previous lives and connections. Unlike other organizations, where names are earned through experiences or character, Diamond Dog names are generated automatically upon recruitment.</p>