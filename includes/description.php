</p>

<h3>Why do Diamond Dog recruits get new names?</h3>
<p>Diamond Dogs, like members of other military organizations, get code names. Unlike other organizations, where names are earned through experiences or character, Diamond Dog names are generated automatically upon recruitment. This automated name conceals and separates a recruit from their previous lives; this also means a recruit is responsible for characterizing what their given name means.</p>